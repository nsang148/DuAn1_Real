    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        String id = txtID.getText();

        if (sevice.deleteTacGia(id) == 1) {
            JOptionPane.showMessageDialog(this, "Xoa thanh cong");
        } else {
            JOptionPane.showMessageDialog(this, "Xoa that bai");
        }
        loadData();
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        String id = txtID.getText();
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        TacGia tg = new TacGia(id, ma, ten);
        if (sevice.updateTacGia(tg) == 1) {
            JOptionPane.showMessageDialog(this, "Update thanh cong");
        } else {
            JOptionPane.showMessageDialog(this, "Update that bai");
        }
        loadData();
    }//GEN-LAST:event_btnSuaActionPerformed

    private void tblTacGiaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTacGiaMouseClicked
        int selectedRow = tblTacGia.getSelectedRow();
        TacGia tg = this.sevice.getAll().get(selectedRow);
        txtMa.setText(tg.getMa());
        txtTen.setText(tg.getTen());
        txtID.setText(tg.getId());
    }//GEN-LAST:event_tblTacGiaMouseClicked

