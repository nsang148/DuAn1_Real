    private void loadData() {
        model = (DefaultTableModel) tblTacGia.getModel();
        model.setRowCount(0);
        List<TacGia> list = sevice.getAll();
        for (TacGia tg : list) {

            model.addRow(new Object[]{tg.getId(), tg.getMa(), tg.getTen()});
        }
    }
